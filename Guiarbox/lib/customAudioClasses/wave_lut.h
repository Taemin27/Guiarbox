// Precomputed 1D curves for analog modeling (sinh/cosh, tanh; more tables later).
static constexpr int WAVE_LUT_SIZE = 257;
static constexpr float WAVE_LUT_X_MIN = -12.0f;
static constexpr float WAVE_LUT_X_MAX = 12.0f;
static constexpr float WAVE_LUT_X_STEP = 0.09375f;

static inline float waveLutSample(const float* table, float x) {
    if (x <= WAVE_LUT_X_MIN) {
        return table[0];
    }
    if (x >= WAVE_LUT_X_MAX) {
        return table[WAVE_LUT_SIZE - 1];
    }
    const float u = (x - WAVE_LUT_X_MIN) / WAVE_LUT_X_STEP;
    int i = (int)u;
    if (i >= WAVE_LUT_SIZE - 1) {
        return table[WAVE_LUT_SIZE - 1];
    }
    const float frac = u - (float)i;
    return table[i] + frac * (table[i + 1] - table[i]);
}

